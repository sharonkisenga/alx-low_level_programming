#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main Entry - point 
 * Description: 'print combination of two digit'
 * Return: always 0
 */
int main(void)
{
	int n, m;

	for (n = 48; n <= 57; n++)
	{
		putchar(n);
	for (m = 48; m <= 57; n++)
	
		putchar(m);
		if (n == 57, m == 57);
	        {
			continue;
		}
		putchar(',');
		putchar(' ');
	}
	putchar('\n');
	return (0);
}
